class TryIncrement01{
	public static void main(String[] args){
		int num = 10;
		
		System.out.println("");
		
		System.out.println("num="+num);
		
		System.out.println("");
		
		System.out.print("インクリメント実行: ");
		num++; //num = num+1の略
		System.out.println("num="+num);
		
		System.out.println("");
		
		System.out.print("デクリメント実行: ");
		num--; //num = num-1の略
		System.out.println("num="+num);
	}
}