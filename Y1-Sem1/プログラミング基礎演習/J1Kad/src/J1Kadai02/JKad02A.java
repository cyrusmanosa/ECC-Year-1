class JKad02A{
	public static void main(String[]args){
		int apple;
		apple=10;
		int person;
		person=3;
		System.out.println("リンゴが10 個ありました！");
		System.out.println(apple);
		System.out.println("人間が3 人やってきました！");
		System.out.println(person);
		System.out.println("それぞれが食べたリンゴの数は");
		System.out.println(apple/person);
		System.out.println("残ったリンゴの数は");
		System.out.println(apple%person);
	}
}