class JKad02S{
	public static void main(String[] args){
		int x;
		x=2;
		int y;
		y=7;
		int z;
		z=x;
		System.out.println("x の値は");
		System.out.println(x);
		System.out.println("y の値は");
		System.out.println(y);
		System.out.println("*** x とy の値を入れ替えます！ ***");
		x=y;
		System.out.println("x の値は");
		System.out.println(x);
		y=z;
		System.out.println("y の値は");
		System.out.println(y);
	}
}