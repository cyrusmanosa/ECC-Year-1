class JKad02C{
	public static void main(String[] args){
		int a;
		a=5;
		System.out.println("変数a の値を表示します！");
		System.out.println(a);
		
		int b;
		b=20;
		System.out.println("変数b の値を表示します！");
		System.out.println(b);
		
		b=a;
		System.out.println("変数b にa を代入します！");
		System.out.println("");
		System.out.println("変数a の値を表示します！");
		System.out.println(a);
		System.out.println("変数b の値を表示します！");
		System.out.println(b);
	}
		
}