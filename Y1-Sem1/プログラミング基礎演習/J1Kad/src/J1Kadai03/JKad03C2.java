public class JKad03C2{
	public static void main(String[] args){
		int i = 0;
		System.out.println("現在のi の値\t" + i);
		System.out.println();
		// インクリメント

		System.out.println("i++で表示\t" + (i++));
		System.out.println("現在のi の値\t" + i);
		System.out.println();
		System.out.println("++i で表示\t" + (++i));
		System.out.println("現在のi の値\t" + i);
		System.out.println();
		
		// デクリメント
		System.out.println("i--で表示\t" + (i--));
		System.out.println("現在のi の値\t" + i);
		System.out.println();
		System.out.println("--i で表示\t" + (--i));
		System.out.println("現在のi の値\t" + i);
		System.out.println();
	}
}