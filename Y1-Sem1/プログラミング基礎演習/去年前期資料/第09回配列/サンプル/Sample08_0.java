public class Sample08_0 {
	public static void main(String[] args) {
		/** 宣言 **/
		int numbers[] = {1, 2, 3, 4, 5};// 配列の宣言&初期化
		
		/** 実行処理 **/
		System.out.println("0番目：" + numbers[0]);// numbers配列の0番目を表示
		System.out.println("1番目：");// numbers配列の1番目を表示
		System.out.println("2番目：");// numbers配列の2番目を表示
		System.out.println("3番目：");// numbers配列の3番目を表示
		System.out.println("4番目：");// numbers配列の4番目を表示
	}
}
