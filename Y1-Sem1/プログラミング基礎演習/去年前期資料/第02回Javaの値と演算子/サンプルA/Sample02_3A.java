public class Sample02_3A {
	public static void main(String[] args) {
		System.out.println("◆10進数");
		System.out.println(100 + " + " + 10 + " = " + 110);
		System.out.println();
		
		System.out.println("◆2進数");
		System.out.println(0b100 + " + " + 0b10 + " = " + 0b110);
		System.out.println();
		
		System.out.println("◆8進数");
		System.out.println(0100 + " + " + 010 + " = " + 0110);
		System.out.println();
		
		System.out.println("◆16進数");
		System.out.println(0x100 + " + " + 0x10 + " = " + 0x110);
	}
}
