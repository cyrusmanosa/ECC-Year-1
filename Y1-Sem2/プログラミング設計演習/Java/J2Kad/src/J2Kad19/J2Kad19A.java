import java.util.Scanner;
public class J2Kad19A {
    enum BALL {STRAIGHT, CURVE, FORKBALL}
    public static void main(String[] args) {
        System.out.println("あなたはECC野球部の名キャッチャーです！");
        System.out.println("サインを出してピッチャーの球をキャッチしてください！");
        Scanner in = new Scanner(System.in);
        BALL[] balls = BALL.values();
        while (true) {
            try {
                System.out.print("何のサインを出しますか？（0：直球、1：カーブ、2：フォーク、-1：終了）＞ ");
                int num = Integer.parseInt(in.next());
                if (num < 0) break;
                throwBall(balls[num]);
            } catch (Straigt  e) {
                System.out.println("Straight: ストレートを投げた！");
                System.out.println("キャッチしました！");
            } catch (Curve e) {
                System.out.println("Curve: カーブを投げた！");
                System.out.println("キャッチしました！");
            } catch (Forkball e) {
                System.out.println("ForkBall: フォークボールを投げた！");
                System.out.println("キャッチしました！");
            } catch (Exception z) {System.out.println("そんな球種はありません！");}
            System.out.println();
        }
    }
    public static void throwBall(BALL ball) throws Straigt, Curve, Forkball{
        switch(ball) {
            case STRAIGHT -> throw new Straigt();
            case CURVE -> throw new Curve();
            case FORKBALL -> throw new Forkball();
        }
    }
    private static class Straigt extends Exception { }
    private static class Curve extends Exception { }
    private static class Forkball extends Exception { }
}

