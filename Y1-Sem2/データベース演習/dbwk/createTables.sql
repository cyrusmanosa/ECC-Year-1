/*---------------以下テーブル作成----------------------*/
-- 商品表
CREATE TABLE  PRODUCT(
    PRODUCT_NO CHAR(4),
    PNAME      VARCHAR(50),
    CATEGORY   VARCHAR(4),
    PRICE      INT(5)
);

-- 試作品表
CREATE TABLE  PROTOTYPE(
    PROTOTYPE_NO CHAR(4),
    PROTONAME    VARCHAR(50),
    CATEGORY     VARCHAR(4),
    PRICE        INT(5)
);

-- 材料表
CREATE TABLE  MATERIAL(
    MATERIAL_NO CHAR(5),
    MNAME       VARCHAR(50),
    ORIGIN      VARCHAR(20),
    COST        INT(5)
);

-- レシピ表
CREATE TABLE  RECIPE(
    PRODUCT_NO  CHAR(4),
    MATERIAL_NO CHAR(5),
    QUANTITY    INT(2)
);

-- 店舗表
CREATE TABLE  STORE(
    STORE_NO    CHAR(3),
    SNAME       VARCHAR(20),
    TEL         VARCHAR(10),
    PREFECTURES VARCHAR(3),
    ADDRESS     VARCHAR(100),
    OPEN_TIME   TIME,
    CLOSE_TIME  TIME,
    HOLIDAY     VARCHAR(7),
    STATUS      VARCHAR(5)
);

-- 従業員表
CREATE TABLE  EMPLOYEE(
    EMP_NO     CHAR(5),
    ENAME      VARCHAR(20),
    BIRTHDAY   DATE,
    TEL        VARCHAR(10),
    ADDRESS    VARCHAR(100),
    HIREDATE   DATE,
    JOB        VARCHAR(4),
    RANK_POINT INT(4),
    SALARY     INT(4),
    WORK_STORE CHAR(3),
    MGR_NO     CHAR(5)
);

-- ランク表
CREATE TABLE  RANKPOINT(
    RANK_NAME VARCHAR(6),
    RANK_DESC VARCHAR(50),
    LO_POINT  INT(4),
    HI_POINT  INT(4)
);

-- 旧商品表
CREATE TABLE  OLDPRODUCT(
    PRODUCT_NO CHAR(4),
    PNAME      VARCHAR(50),
    CATEGORY   VARCHAR(4),
    PRICE      INT(5)
);


