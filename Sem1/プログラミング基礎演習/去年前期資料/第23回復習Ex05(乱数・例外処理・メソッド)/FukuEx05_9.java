/*
	引数なし、戻り値なしのメソッドを好きに実装し
	mainメソッドで自作したメソッドを活用するプログラムを作成しなさい
*/


class FukuEx05_9{
	
	/*
		メソッド(引数なし、戻り値なし)
		引数 :なし
		戻り値 :void
	 */
	 public static void jikosyoukai(){
	 	System.out.println("石田です！");
		System.out.println("宜しくお願い致します！");
	 }

	 
	/* メインメソッド */
	public static void main(String args[]){
		jikosyoukai();
	}
}
