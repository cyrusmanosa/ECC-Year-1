/*
int型変数a（初期値：3）と
         b（初期値：5）の
値を入れ替える処理を作成せよ。

（実行結果）
変数aの値は3
変数bの値は5
aとbの値を入れ替えます！えいっ！！
変数aの値は5
変数bの値は3

*/

class Ex03_1{
	public static void main(String[] args){
		// 宣言
		int a = 3;
		int b = 5;
		
		// 値を表示
		System.out.println("変数aの値は" + a);
		System.out.println("変数bの値は" + b);
		
		System.out.println("aとbの値を入れ替えます！えいっ！！");
		
		// 入れ替える処理を記述
		
		
		
		// 値を再表示
		System.out.println("変数aの値は" + a);
		System.out.println("変数bの値は" + b);
		
	}
}