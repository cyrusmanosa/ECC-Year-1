class Sample03_1a{
	public static void main(String[] args){	
		/* 変数の宣言 */
		// byte型で宣言を行い、値を代入
		byte a = 123;
		
		// short型で宣言を行い、値を代入
		short b = 4567;
		
		// int型で宣言を行い、値を代入
		int c = 1234567890;
		
		/* 表示 */
		// 文字列と変数aの値を表示
		System.out.println("byte:" + a);
		
		// 文字列と変数bの値を表示
		System.out.println("short:" + b);
		
		// 文字列と変数cの値を表示
		System.out.println("int:" + c);
	}
}