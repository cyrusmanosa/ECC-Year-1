/*
int型変数a（初期値：3）
         b（初期値：5）
		 c（初期値：7）
の値をローテーションさせる処理を作成せよ。

（実行結果）
変数aの値は3
変数bの値は5
変数cの値は7
aとbとcの値をローテーションさせます！むむっ！！
変数aの値は5
変数bの値は7
変数cの値は3

*/

class Ex03_2{
	public static void main(String[] args){
		// 宣言
		int a = 3;
		int b = 5;
		int c = 7;
		
		// 値を表示
		System.out.println("変数aの値は" + a);
		System.out.println("変数bの値は" + b);
		System.out.println("変数bの値は" + c);
		
		System.out.println("aとbとcの値をローテーションさせます！むむっ！！");
		
		// 入れ替える処理を記述
		
		
		
		// 値を再表示
		System.out.println("変数aの値は" + a);
		System.out.println("変数bの値は" + b);
		System.out.println("変数bの値は" + c);
		
	}
}